#ifndef UNTITLED_ADDRESS_H
#define UNTITLED_ADDRESS_H

#include <string>

using namespace std;

struct Address {
    string country;
    string region;
    string city;
    string street;
    string house;
    int post_code;

    Address(){}
    Address(string country, string region, string city, string street, string house, int post_code)
    {
        return country;
        return region;
        return city;
        return street;
        return house;
        return post_code;
    }
};



#endif //UNTITLED_ADDRESS_H
